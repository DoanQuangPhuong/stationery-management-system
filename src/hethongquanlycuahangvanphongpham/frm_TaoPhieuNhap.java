    private void themChiTietPhieuNhap()
    {
        String sqlChiTietPN = "insert into htquanlycuahangvanphongpham.chitietphieunhap(MaPN, MaSanPham, SoLuong, Gia) values (?,?,?,?)";
        String updateSLSanPham = "UPDATE htquanlycuahangvanphongpham.sanpham SET SoLuongTon = SoLuongTon + ? " + " WHERE MaSanPham = ?";
        Connection con = ConnectionDB.getConnectDB();
        try {
            pst = con.prepareStatement(sqlChiTietPN);
            pstUpdate = con.prepareStatement(updateSLSanPham);
            DefaultTableModel model = (DefaultTableModel) tb_ChiTietPhieuNhap.getModel();
            int rowCount = model.getRowCount();
            //Duyệt từng dòng dữ liệu trong Jtable tb_ChiTietPhieuNhap
            for (int row = 0; row < rowCount; row++) {
                //Thêm chi tiết phiếu nhập
                pst.setString(1, tf_MaPN.getText());
                pst.setString(2, listCTPN.get(row).getMaSanPham());
                pst.setInt(3, listCTPN.get(row).getSoLuong());
                pst.setFloat(4, listCTPN.get(row).getGia());
                pst.executeUpdate();
                //Cập nhật số lượng trong sản phẩm
                pstUpdate.setInt(1, listCTPN.get(row).getSoLuong());
                pstUpdate.setString(2, listCTPN.get(row).getMaSanPham());
                pstUpdate.executeUpdate();
            }   
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        ConnectionDB.closeConnectionDB(con);
    }
