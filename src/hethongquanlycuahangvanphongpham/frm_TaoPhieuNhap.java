    private float tongTien()
    {
        float tongTien = 0;
        for(ChiTietPN i:listCTPN)
        {
            tongTien += i.getSoLuong() * i.getGia();
        }
        return tongTien;
    }
    
    private void btn_ThemSanPhamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemSanPhamActionPerformed
        try {
        ChiTietPN ct = new ChiTietPN();
        String tenSp = (String)cb_SanPham.getSelectedItem();
        int soLuong = Integer.parseInt(tf_SoLuong.getText());
        float gia = Float.parseFloat(tf_Gia.getText());
        ct.setMaSanPham(listSP.get(cb_SanPham.getSelectedIndex()).getMaSP());
        ct.setTenSanPham(tenSp);
        ct.setSoLuong(soLuong);
        ct.setGia(gia);
        ct.setMaPhieuNhap(tf_MaPN.getText());
        listCTPN.add(ct);
        lb_TongTienPN.setText(String.valueOf(tongTien()));
        DefaultTableModel modelTable = (DefaultTableModel) tb_ChiTietPhieuNhap.getModel();
        modelTable.setRowCount(0);
        showDataTable();
        clearTextFill(); 
        JOptionPane.showMessageDialog(null, "Thêm Dữ Liệu vào bảng Thành Công");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Thêm Dữ Liệu vào bảng Thất Bại");
            tf_SoLuong.requestFocus();
        }
    }//GEN-LAST:event_btn_ThemSanPhamActionPerformed

    
