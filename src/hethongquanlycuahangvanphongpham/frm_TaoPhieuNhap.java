    public void loadCombobox() throws SQLException
    {
        String strNCC = "SELECT * FROM htquanlycuahangvanphongpham.nhacungcap;";
        String strSP="SELECT * FROM htquanlycuahangvanphongpham.sanpham;";
        Connection conn = ConnectionDB.getConnectDB();//Mở kết nối
        ResultSet result = ConnectionDB.getSelectResult(conn,strNCC);
        ResultSet resultSP = ConnectionDB.getSelectResult(conn,strSP);
        while(result.next())
        {
            String MaNCC = result.getString("MaNCC");
            String TenNCC = result.getString("TenNCC");
            NhaCungCap n = new NhaCungCap();
            n.setMaNCC(MaNCC);
            n.setTenNCC(TenNCC);
            listNCC.add(n);
        }
        for(int i = 0;i < listNCC.size();i++)
        {
            cb_NhaCC.addItem(listNCC.get(i).getTenNCC());
        }
        
        
          while(resultSP.next())
        {
            String MaSP = resultSP.getString("MaSanPham");
            String TenSP= resultSP.getString("TenSanPham");
            SanPham s = new SanPham();
            s.setMaSP(MaSP);
            s.setTenSP(TenSP);
            listSP.add(s);
        }
        for(int i = 0;i < listSP.size();i++)
        {
            cb_SanPham.addItem(listSP.get(i).getTenSP());
        }
        ConnectionDB.closeConnectionDB(conn);//Đóng kết nối
    }
