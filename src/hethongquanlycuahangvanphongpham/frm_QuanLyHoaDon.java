    public void LoadData() throws SQLException
        {
            Connection con = ConnectionDB.getConnectDB();
            String sql = "SELECT hd.MaHD, hd.ThanhTien, hd.NgayMua, nv.TenNhanVien " +
             "FROM htquanlycuahangvanphongpham.hoadon hd " +
             "INNER JOIN htquanlycuahangvanphongpham.nhanvien nv ON hd.MaNhanVien = nv.MaNhanVien";
            try
            {
            pst = con.prepareStatement(sql);
            resultSet = pst.executeQuery();
            
            ResultSetMetaData stData = resultSet.getMetaData();
            columns = stData.getColumnCount();
            DefaultTableModel Table = (DefaultTableModel)tb_HoaDon.getModel();
            Table.setRowCount(0);
            while(resultSet.next()){
                Vector columnData = new Vector();
                for(int i=1; i<=columns; i++)
                {
                    columnData.add(resultSet.getString("MaHD"));
                    columnData.add(resultSet.getString("ThanhTien"));
                    columnData.add(resultSet.getString("NgayMua")); 
                    columnData.add(resultSet.getString("TenNhanVien"));
                }
                Table.addRow(columnData);
                }
            }
            catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }
            ConnectionDB.closeConnectionDB(con);
        }
