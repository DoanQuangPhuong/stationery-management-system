    private void tb_HoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_HoaDonMouseClicked
        DefaultTableModel TablePN = (DefaultTableModel)tb_HoaDon.getModel();
        int SelectedRows = tb_HoaDon.getSelectedRow();
        Connection con = ConnectionDB.getConnectDB();
            String sql = "SELECT cthd.MaHD, sp.TenSanPham, cthd.SoLuong, cthd.Gia " +
             "FROM htquanlycuahangvanphongpham.chitiethoadon cthd " +
             "INNER JOIN htquanlycuahangvanphongpham.hoadon hd ON cthd.MaHD = hd.MaHD " +
             "INNER JOIN htquanlycuahangvanphongpham.sanpham sp ON cthd.MaSanPham = sp.MaSanPham " +
             "WHERE cthd.MaHD = ?";
            try
            {
            pst = con.prepareStatement(sql);
            pst.setString(1, TablePN.getValueAt(SelectedRows, 0).toString());
            resultSet = pst.executeQuery();
            
            ResultSetMetaData stData = resultSet.getMetaData();
            columns = stData.getColumnCount();
            DefaultTableModel TableCTPN = (DefaultTableModel)tb_ChiTietHoaDon.getModel();
            TableCTPN.setRowCount(0);
            while(resultSet.next()){
                Vector columnData = new Vector();
                for(int i=1; i<=columns; i++)
                {
                    columnData.add(resultSet.getString("MaHD"));
                    columnData.add(resultSet.getString("TenSanPham"));
                    columnData.add(resultSet.getString("SoLuong"));
                    columnData.add(resultSet.getString("Gia")); 
                }
                TableCTPN.addRow(columnData);
                }
            }
            catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        ConnectionDB.closeConnectionDB(con);
    }//GEN-LAST:event_tb_HoaDonMouseClicked

