    private void btn_SuaSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaSPActionPerformed
        try {
            DefaultTableModel RecordTable = (DefaultTableModel)tb_ChiTietHoaDon.getModel();
            int selectedRow = tb_ChiTietHoaDon.getSelectedRow();
            listCTHD.get(selectedRow).setSoLuong(Integer.parseInt(tf_SoLuong.getText()));
            listCTHD.get(selectedRow).setGia(Float.parseFloat(tf_Gia.getText()));
            lb_TongTienHD.setText(String.valueOf(tongTien()));
            DefaultTableModel modelTable = (DefaultTableModel) tb_ChiTietHoaDon.getModel();
            modelTable.setRowCount(0);
            clearTextFill();
            showDataTable();
            JOptionPane.showMessageDialog(null, "Sửa Dữ Liệu vào bảng Thành Công");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sửa Dữ Liệu vào bảng Thất Bại");
            tf_SoLuong.requestFocus();
        }
    }//GEN-LAST:event_btn_SuaSPActionPerformed

    private void btn_XoaSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaSPActionPerformed
        try {
            DefaultTableModel RecordTable = (DefaultTableModel)tb_ChiTietHoaDon.getModel();
            int selectedRow = tb_ChiTietHoaDon.getSelectedRow();
            listCTHD.remove(selectedRow);
            DefaultTableModel modelTable = (DefaultTableModel) tb_ChiTietHoaDon.getModel();
            modelTable.setRowCount(0);
            clearTextFill();
            showDataTable();
            JOptionPane.showMessageDialog(null, "Xóa Dữ Liệu vào bảng Thành Công");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Không có dữ liệu trong bảng !!!", "Error", JOptionPane.ERROR_MESSAGE);

            tf_SoLuong.requestFocus();
        }
    }//GEN-LAST:event_btn_XoaSPActionPerformed

