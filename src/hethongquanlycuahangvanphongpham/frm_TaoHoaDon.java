    private void themChiTietHoaDon()
    {
        String sqlChiTietPN = "insert into htquanlycuahangvanphongpham.chitiethoadon(MaHD, MaSanPham, SoLuong, Gia) values (?,?,?,?)";
        String updateSLSanPham = "UPDATE htquanlycuahangvanphongpham.sanpham SET SoLuongTon = SoLuongTon - ? " + " WHERE MaSanPham = ?";
        Connection con = ConnectionDB.getConnectDB();
        try {
            pst = con.prepareStatement(sqlChiTietPN);
            pstUpdate = con.prepareStatement(updateSLSanPham);
            DefaultTableModel model = (DefaultTableModel) tb_ChiTietHoaDon.getModel();
            int rowCount = model.getRowCount();
            for (int row = 0; row < rowCount; row++) {
                //Thêm chi tiết phiếu nhập
                pst.setString(1, tf_MaHD.getText());
                pst.setString(2, listCTHD.get(row).getMaSP());
                pst.setInt(3, listCTHD.get(row).getSoLuong());
                pst.setFloat(4, listCTHD.get(row).getGia());
                pst.executeUpdate();
                //Cập nhật số lượng trong sản phẩm
                pstUpdate.setInt(1, listCTHD.get(row).getSoLuong());
                pstUpdate.setString(2, listCTHD.get(row).getMaSP());
                pstUpdate.executeUpdate();
            }   
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Thêm chi tiết hóa đơn thất bại lỗi :" + ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
        ConnectionDB.closeConnectionDB(con);
    }
    private float tongTien()
    {
        float tongTien = 0;
        for(ChiTietHoaDon i:listCTHD)
        {
            tongTien += i.getSoLuong() * i.getGia();
        }
        return tongTien;
    }
