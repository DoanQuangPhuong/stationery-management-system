    private void tb_ChiTietHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_ChiTietHoaDonMouseClicked
        DefaultTableModel RecordTable = (DefaultTableModel)tb_ChiTietHoaDon.getModel();
        int selectedRow = tb_ChiTietHoaDon.getSelectedRow();
        String loaiSP = RecordTable.getValueAt(selectedRow, 0).toString();
        for (int i = 0; i < cb_SanPham.getItemCount(); i++) {
            if (cb_SanPham.getItemAt(i).toString().equals(loaiSP)) {
                cb_SanPham.setSelectedIndex(i);
                break;
            }
        }
        tf_SoLuong.setText(RecordTable.getValueAt(selectedRow, 1).toString());
        tf_Gia.setText(RecordTable.getValueAt(selectedRow, 2).toString());
    }//GEN-LAST:event_tb_ChiTietHoaDonMouseClicked

