    public void loadData ()
    {
        
        Connection con = ConnectionDB.getConnectDB();
        String sql = "Select * from htquanlycuahangvanphongpham.nhacungcap";
        try{
            int number;
            Vector row, column;
            column = new Vector();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();
            for(int i =1;i <= number ; i++)
                {
                column.add(metadata.getColumnName(i));
            }
            tbn.setColumnIdentifiers(column);
            while(rs.next()){
                row = new Vector();
                for(int i =1; i<= number; i++)
                {
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                table2.setModel(tbn);
            }
            table2.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if(table2.getSelectedRow() >= 0)
                    {
                        tf_Ma.setText(table2.getValueAt(table2.getSelectedRow(),0)+"");
                        tf_Ma.setEditable(false);
                        tf_Ten.setText(table2.getValueAt(table2.getSelectedRow(),1)+"");
                        tf_SDT.setText(table2.getValueAt(table2.getSelectedRow(),2)+"");
                        tf_DiaChi.setText(table2.getValueAt(table2.getSelectedRow(),3)+"");
                        tf_Email.setText(table2.getValueAt(table2.getSelectedRow(),4)+"");
                        
                    }
                }
            });
        }catch(Exception ex)
        {
            System.out.println(ex.toString());
        }
        ConnectionDB.closeConnectionDB(con);
    }

