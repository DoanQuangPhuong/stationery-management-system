    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        // TODO add your handling code here:
        Connection con = ConnectionDB.getConnectDB();
        String checkMaNCc = "SELECT * FROM htquanlycuahangvanphongpham.nhacungcap WHERE MaNCC=?";
        String sql = "insert into htquanlycuahangvanphongpham.nhacungcap(MaNCC, TenNCC, SDT, DiaChi, Email) value(?, ?, ?, ?, ?)";
        try{
            
            pst = con.prepareStatement(checkMaNCc);
            pst.setString(1, tf_Ma.getText());
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Mã Nhà Cung Cấp Đã Tồn Tại!!!");
                return; // Dừng thực hiện nếu tên người dùng đã tồn tại
            }
            pst = con.prepareStatement(sql);
            pst.setString(1, tf_Ma.getText());
            pst.setString(2, tf_Ten.getText());
            pst.setString(3, tf_SDT.getText());
            pst.setString(4, tf_DiaChi.getText());
            pst.setString(5, tf_Email.getText());
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Da hoan thanh them moi");
            tbn.setRowCount(0);
            loadData();
        }catch(Exception ex)
        {
            System.out.println(ex.toString());
        }
        ConnectionDB.closeConnectionDB(con);
        
    }//GEN-LAST:event_btn_ThemActionPerformed

