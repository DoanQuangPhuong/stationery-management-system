    public void loadData() throws SQLException
    {
        Connection con = ConnectionDB.getConnectDB();
        String sql = "Select * from htquanlycuahangvanphongpham.loai";
        try{
            int number;
            Vector row ,column;
            column = new Vector();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();
            
            for(int i =1;i <= number ; i++)
            {
                column.add(metadata.getColumnName(i));
            }
            tbn.setColumnIdentifiers(column);
            while(rs.next()){
                row = new Vector();
                for(int i =1; i<= number; i++)
                {
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                table.setModel(tbn);
            }
            
            table.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if(table.getSelectedRow() >= 0)
                    {
                        tf_Ma.setText(table.getValueAt(table.getSelectedRow(),0)+"");
                        tf_Ma.setEditable(false);
                        tf_Ten.setText(table.getValueAt(table.getSelectedRow(),1)+"");
                    }
                }
            });
        }catch(Exception ex)
        {
            System.out.println(ex.toString());
        }
        
        ConnectionDB.closeConnectionDB(con);
    }
    

