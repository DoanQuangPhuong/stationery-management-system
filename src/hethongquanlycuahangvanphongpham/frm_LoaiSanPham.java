    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        // TODO add your handling code here:
        Connection con = ConnectionDB.getConnectDB();
        String checkMaSanPham= "SELECT * FROM htquanlycuahangvanphongpham.loai WHERE MaLoai=?";
        String sql = "insert into htquanlycuahangvanphongpham.loai(MaLoai, TenLoai) value(?, ?)";
        try{
            pst = con.prepareStatement(checkMaSanPham);
            pst.setString(1, tf_Ma.getText());
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Mã sản Phẩm đã tồn tại.");
                return; // Dừng thực hiện nếu tên người dùng đã tồn tại
            }
            pst = con.prepareStatement(sql);
            pst.setString(1, tf_Ma.getText());
            pst.setString(2, tf_Ten.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Da hoan thanh them moi");
            tbn.setRowCount(0);
            loadData();
        }catch(Exception ex)
        {
            System.out.println(ex.toString());
        }
        ConnectionDB.closeConnectionDB(con);
    }//GEN-LAST:event_btn_ThemActionPerformed

